function currencyDKK(amount: number) {
  return new Intl.NumberFormat('da-DK', { style: 'currency', currency: 'DKK', maximumFractionDigits: 0 }).format(amount);
}

// ── Print helpers ─────────────────────────────────────────────────────────────

function printHTML(title: string, body: string) {
  const w = window.open('', '_blank');
  if (!w) return;
  w.document.write(`
    <!DOCTYPE html>
    <html>
    <head>
      <title>${title}</title>
      <meta charset="utf-8" />
      <style>
        body { font-family: Arial, sans-serif; font-size: 13px; color: #111; margin: 32px; }
        h1 { font-size: 20px; margin-bottom: 4px; }
        h2 { font-size: 15px; margin: 20px 0 6px; border-bottom: 1px solid #ccc; padding-bottom: 4px; }
        table { width: 100%; border-collapse: collapse; margin-top: 8px; }
        th, td { text-align: left; padding: 6px 10px; border-bottom: 1px solid #e5e5e5; }
        th { background: #f3f4f6; font-weight: 600; }
        .badge { display: inline-block; padding: 2px 8px; border-radius: 12px; font-size: 11px; font-weight: 500; }
        .green { background: #d1fae5; color: #065f46; }
        .amber { background: #fef3c7; color: #92400e; }
        .footer { margin-top: 40px; font-size: 11px; color: #999; border-top: 1px solid #e5e5e5; padding-top: 8px; }
        @media print { button { display: none; } }
      </style>
    </head>
    <body>
      ${body}
      <div class="footer">Generated by PourStock · ${format(new Date(), 'PPpp')}</div>
      <script>window.onload = () => window.print();</script>
    </body>
    </html>
  `);
  w.document.close();
}

